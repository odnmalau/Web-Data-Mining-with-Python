# Meminta pengguna untuk memasukkan sebuah angka dan mengonversinya menjadi tipe integer
i = int(input("Enter a number"))

# Mencetak nilai dari variabel 'i' yang dimasukkan oleh pengguna
print(i)

# Menambahkan 1 ke nilai 'i' dan menyimpan hasilnya kembali ke 'i'
i = i + 1

# Mencetak nilai baru dari variabel 'i' setelah ditambahkan 1
print(i)

# Mendefinisikan string 's' dengan nilai "Welcome to Python"
s = "Welcome to Python"

# Mencetak nilai dari string 's'
print(s)