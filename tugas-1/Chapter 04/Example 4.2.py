# Mendefinisikan variabel 'i' dengan nilai 5
i = 5

# Mendefinisikan variabel 'j' dengan nilai 2
j = 2

# Memeriksa apakah nilai 'i' kurang dari 'j'
if i < j:
    # Jika kondisi benar, cetak "i is smaller than j"
    print("i is smaller than j")
else:
    # Jika kondisi salah, cetak nilai 'i' dan 'j' dengan format "i is greater than j"
    print(i, "is greater than", j)